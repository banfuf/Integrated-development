package NASA;

public class GetSwitch {
	public static void main(String args[]) {
		int dex = 5;
		switch (dex) {
		case 1:
			System.out.println("0");
			break;
		case 3:
			System.out.println("1");
			break;
		case 5:
			System.out.println("0");
			break;
		default:
			System.out.println("1");
		}
	}
}