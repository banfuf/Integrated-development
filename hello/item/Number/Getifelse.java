package Number;

public class Getifelse { // 定义类
	public static void main(String[] args) { // 主方法
		int math = 95;
		int english = 56;
		if (math > 60) {
			System.out.println("数学及格了");
		} else {
			System.out.println("数学没有及格");
		}
		if (english > 60) {
			System.out.println("英语及格了");
		} else {
			System.out.println("英语没有及格");
		}
	}
}
